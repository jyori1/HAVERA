var timeleft = 10;
var downloadTimer = setInterval(function(){
  document.getElementById("countdown10").innerHTML = timeleft;
  timeleft -= 1;
  if(timeleft <= -1){
    clearInterval(downloadTimer);
    document.getElementById("countdown10").innerHTML = "Finished"
  }
}, 1000);

